package String;

public class StringDemo {

	public static void main(String[] args) {
	
		// char 형 출력
		System.out.println('출');
		
		// String 형 출력
		System.out.println("력해보기");
		
		// 특수문자 출력하기
		System.out.println("\"Hello Java\"");
		
		String a, b;
		a = "코딩";
		b = "연습하기";
		System.out.println(a+b);
	}
}
